package com.lgin.qb.phd.Request;

public class Passwordsettings {
	
	private String password;
	private String cnfpass;
	private String newpassword;
	private String cnfnewpass;
	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	public String getCnfpass() {
		return cnfpass;
	}
	public void setCnfpass(String cnfpass) {
		this.cnfpass = cnfpass;
	}
	public String getNewpassword() {
		return newpassword;
	}
	public void setNewpassword(String newpassword) {
		this.newpassword = newpassword;
	}
	public String getCnfnewpass() {
		return cnfnewpass;
	}
	public void setCnfnewpass(String cnfnewpass) {
		this.cnfnewpass = cnfnewpass;
	}
	
	
}
